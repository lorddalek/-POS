    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        checkTime();
        if (!txtCode.getText().equals("")){
        DecimalFormat df = new DecimalFormat("#.00");
        openDatabase();
        String query = "SELECT * FROM Products WHERE Barcode = '" + txtCode.getText() + "'";
        try{
            PreparedStatement pst = con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            if (rs.next()){ 
                if(!rs.getString(8).equals("1")){
                    if(rs.getString(7).equals("1")){
                        if(accessLevel == 3){
                            if(transactionAuthorised == false){
                            codeOnHold = rs.getInt(4);
                            SupervisorIntervention supvIntervention = new SupervisorIntervention(2, this);
                            supvIntervention.pack();
                            supvIntervention.setLocationRelativeTo(null);
                            supvIntervention.setVisible(true);                            
                            btnCheckAuthorisation.setEnabled(true);
                            txtCode.setText("");
                            return;
                            }
                        }
                        else{
                            if (transactionAuthorised == false){
                             javax.swing.JOptionPane.showMessageDialog(this, "Restricted Item Check Age", "ALERT", javax.swing.JOptionPane.WARNING_MESSAGE);
                             transactionAuthorised = true;
                        }
                        }
                    }
                double price = rs.getDouble(5);
                double promotion = 0;
                double promoDiscount = 0;
                productListModel.addElement(rs.getString(2));
                priceListModel.addElement("$" + df.format(price));
                lstProducts.setModel(productListModel);
                lstPrices.setModel(priceListModel);
                String test = rs.getString(5);
                if (!rs.getString(6).equals("0.0000")){
                    promotion = rs.getDouble(6);
                    promoDiscount = (price - promotion);
                    productListModel.addElement("      ON PROMOTION");
                    priceListModel.addElement("-$" + df.format(promoDiscount));
                    lstProducts.setModel(productListModel);
                    lstPrices.setModel(priceListModel);
                }
                totalPrice = totalPrice + price - promoDiscount;
                String stringPrice = df.format(totalPrice);
                lblTotalPrice.setText("$" + stringPrice);

                txtCode.setText("");
            }
                else{
                    javax.swing.JOptionPane.showMessageDialog(this, "Item Sale Prohibited", "Do Not Sell", javax.swing.JOptionPane.ERROR_MESSAGE);
                    txtCode.setText("");
                }
            }
            else{
                javax.swing.JOptionPane.showMessageDialog(this, "Item not found", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                txtCode.setText("");
            }
        }
        catch (Exception e){
            e.printStackTrace();
        }
        closeDatabase();
        }
    }//GEN-LAST:event_btnEnterActionPerformed

    private void btnCheckAuthorisationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckAuthorisationActionPerformed
        if (transactionAuthorised == true){
            txtCode.setText(String.valueOf(codeOnHold));
            javax.swing.JOptionPane.showMessageDialog(this, "Press Enter Again to insert into transaction", "Message", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            codeOnHold = 0;
            btnCheckAuthorisation.setEnabled(false);
            
        }
        else{
            javax.swing.JOptionPane.showMessageDialog(this, "Transaction not authorised", "Message", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            codeOnHold = 0;
            btnCheckAuthorisation.setEnabled(false);
            
        }
    }//GEN-LAST:event_btnCheckAuthorisationActionPerformed

