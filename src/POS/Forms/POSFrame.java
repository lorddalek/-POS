    private void btnCancelItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelItemActionPerformed
        DecimalFormat df = new DecimalFormat("#.00");
        int selectedItem = lstProducts.getSelectedIndex();
        lstPrices.setSelectedIndex(selectedItem);
        double price = Double.parseDouble(lstPrices.getSelectedValue().substring(1));
        lstProducts.setSelectedIndex(selectedItem + 1);
        if (lstProducts.getSelectedValue().equals("      ON Promotion")){
            productListModel.removeElementAt(selectedItem);
            priceListModel.removeElementAt(lstProducts.getSelectedIndex());
        }
        else{
            lstProducts.setSelectedIndex(selectedItem - 1);
        }
        productListModel.removeElementAt(selectedItem);
        priceListModel.removeElementAt(selectedItem);
        lstProducts.setModel(productListModel);
        lstPrices.setModel(priceListModel);
        totalPrice = totalPrice - price;
        String stringPrice = df.format(totalPrice);
        lblTotalPrice.setText("$" + stringPrice);
        
    }//GEN-LAST:event_btnCancelItemActionPerformed

